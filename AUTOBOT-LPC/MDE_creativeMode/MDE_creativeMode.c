#include "Flags.h"
#include "PR_motor.h"

/***********************************************************************************************************************************
 *** MACROS
 **********************************************************************************************************************************/
enum{
	UP = 8,
	UP_LEFT = 9,
	UP_RIGHT = 10,
	DOWN = 4,
	DOWN_LEFT = 5,
	DOWN_RIGHT = 6,
	STEADY = 0
};

#define CREATIVE_SPEED 225

/***********************************************************************************************************************************
 *** PROTOTIPOS DE FUNCIONES
 **********************************************************************************************************************************/
void MDE_creativeMode()
{
	switch (directionFlag) {
		case UP:
			motor_forward(CREATIVE_SPEED);
			break;
		case DOWN:
			motor_backward(CREATIVE_SPEED);
			break;
		case UP_RIGHT:
			motor_go_up_right();
			break;
		case UP_LEFT:
			motor_go_up_left();
			break;
		case DOWN_LEFT:
			motor_go_down_left();
			break;
		case DOWN_RIGHT:
			motor_go_down_right();
			break;
		case STEADY:
			motor_stop();
			break;
		default:directionFlag = STEADY;
			break;
	}
}


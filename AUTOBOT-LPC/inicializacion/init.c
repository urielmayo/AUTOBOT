void autobot_init(void)
{
 	SystemInit();
	flags_init();
	SysTick_Init(1);
	motor_init();
	sensorInfrarojo_init();
	LED_init();
	Lcd_Init();
	timer_init(TIMER_0);
	timer_init(TIMER_1);

	Lcd_SetCursorPosition(LCD_COLUMN1,LCD_ROW1);
	Lcd_printf("INFO2 - AUTOBOT");
	Lcd_SetCursorPosition(LCD_COLUMN1, LCD_ROW2);
	Lcd_printf("Iniciando...");
}

